function fOla() {
  console.log("Olá, mundo!");
}

function fOlaNome(nome) {
  console.log(`Olá, ${nome}!`);
}

function fDobro(valor) {
  console.log(valor * 2);
}

function fMedia(a, b, c) {
  console.log((a + b + c) / 3);
}

function fMaior(a, b) {
  if (a > b) {
    console.log(a);
  } else {
    console.log(b);
  }
}

function fQuadrado(valor) {
  console.log(valor * valor);
}
